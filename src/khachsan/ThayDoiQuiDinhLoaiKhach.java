    public String PhatSinhMaLoaiKhach(List<String> dsp){
        Boolean check=false;
        int a=0;
        String maloaikhach="LK";
        String mlkphatsinh=null;
        int j=1;
        if(dsp.size()==0){
            return "LK1";
        }
        while(check==false){
            a=0;
            for(int i=0;i<dsp.size();i++){
                mlkphatsinh=maloaikhach+""+j;
                if(dsp.get(i).equals(mlkphatsinh)){
                    break;
                }
                else
                    a++;
            }
            j++;
            if(a==dsp.size())
                check=true;
        }
        return mlkphatsinh;
    }
    
    public String MaLoaiKhach(){
        String url="SELECT MaLoaiKhach FROM LOAIKHACH";
        List<String> dsp=new ArrayList<>();
        try{
    
            ResultSet rs=cn.ExcuteQuery(url);
            while(rs.next()){
                dsp.add(rs.getString(1));  
            }        
        }
        catch(Exception ex){
            ex.printStackTrace();
        }

        return PhatSinhMaLoaiKhach(dsp);
    }
