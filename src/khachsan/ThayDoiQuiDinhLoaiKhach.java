    public Boolean XoaNhungThongTinLienQuan(String a,List<String> b){
        String chuoi1="";
        String chuoi2="";
        b.add("Không xóa được\nDo Mã Loại Khách "+a+" có trong Phiếu Thuê Phòng: ");
        try{
            String url="SELECT DISTINCT PHIEUTHUEPHONG.MaPhieuThuePhong\n" +
                        "FROM PHIEUTHUEPHONG,LOAIKHACH,CT_PHIEUTHUEPHONG\n" +
                        "WHERE LOAIKHACH.MaLoaiKhach=CT_PHIEUTHUEPHONG.MaLoaiKhach AND PHIEUTHUEPHONG.MaPhieuThuePhong=CT_PHIEUTHUEPHONG.MaPhieuThuePhong AND LOAIKHACH.MaLoaiKhach='"+a+"'";
            ResultSet rs=cn.ExcuteQuery(url);
            while(rs.next()){
                chuoi1=chuoi1+rs.getString(1)+"  ";
            }
            if(chuoi1.equals("")==false){
                b.set(0, b.get(0)+chuoi1);
            }
            url="SELECT DISTINCT CT_HOADON.MaHoaDon\n" +
                        "FROM PHIEUTHUEPHONG,CT_HOADON,LOAIKHACH,CT_PHIEUTHUEPHONG\n" +
                        "WHERE LOAIKHACH.MaLoaiKhach=CT_PHIEUTHUEPHONG.MaLoaiKhach AND PHIEUTHUEPHONG.MaPhieuThuePhong=CT_PHIEUTHUEPHONG.MaPhieuThuePhong AND PHIEUTHUEPHONG.MaPhieuThuePhong=CT_HOADON.MaPhieuThuePhong AND LOAIKHACH.MaLoaiKhach='"+a+"'";
            rs=cn.ExcuteQuery(url);
            while(rs.next()){
                chuoi2=chuoi2+rs.getString(1)+"  ";
            }
            if(chuoi2.equals("")==false){
                    b.set(0, b.get(0)+"\nVà có trong Hóa Đơn: "+chuoi2);
            }
        }catch(Exception ex){
            ex.printStackTrace();
        }
        if(chuoi1.equals("")==false||chuoi2.equals("")==false)
            return false;
        return true;
    }
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int[] index = jT_LoaiKhach.getSelectedRows();
        if(index.length==0){
            JOptionPane.showMessageDialog(null, "Chưa chọn hàng để xóa");           
        }
        else{     
            model = (DefaultTableModel) jT_LoaiKhach.getModel();
            System.out.println(model.getRowCount());
            try {
                int dem=0;
                for (int i = 0; i < index.length; i++) {
                    List<String> b=new ArrayList<>();
                    if(XoaNhungThongTinLienQuan(model.getValueAt((index[i]-dem), 0).toString(),b)==false){
                        JOptionPane.showMessageDialog(null, b.get(0));
                    }
                    else{
                        url="DELETE FROM LOAIKHACH WHERE MaLoaiKhach='"+model.getValueAt((index[i]-dem), 0).toString()+"'";
                        cn.ExcuteUpdate(url);   
                        model.removeRow(index[i]-dem);
                        dem++;
                    }  
                }
                
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
        txtMaLoaiKhach.setText(MaLoaiKhach());
    }//GEN-LAST:event_jButton2ActionPerformed

